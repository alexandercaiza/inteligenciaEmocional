    private void btnSiguiente9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguiente9ActionPerformed
       if(!validar.preguntaUno || !validar.preguntaDos || !validar.preguntaTres ){
             JOptionPane.showMessageDialog(null, "Por favor, seleccione todas las preguntas");
        
        
        }
        else {
         cuestionarioNueve.retornarSubtotal();
        cuestionarioNueve.retornarTotal();
        
//        JOptionPane.showMessageDialog(null, "El subtotal es "+cuestionarioNueve.retornarSubtotal() );
//        JOptionPane.showMessageDialog(null, "El subtotal es "+cuestionarioNueve.retornarTotal() );
        VentanaPreguntas10 ventanaDiez = new VentanaPreguntas10(cuestionarioNueve.retornarTotal());
        ventanaDiez.setVisible(true);
        this.dispose();
        }
    }//GEN-LAST:event_btnSiguiente9ActionPerformed


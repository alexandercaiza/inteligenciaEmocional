    private void btnSiguiente15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguiente15ActionPerformed
        if (!validar.preguntaUno || !validar.preguntaDos || !validar.preguntaTres) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione todas las preguntas");

        } else {
            cuestionarioQuince.retornarSubtotal();
            cuestionarioQuince.retornarTotal();

//            JOptionPane.showMessageDialog(null, "El subtotal es " + cuestionarioQuince.retornarSubtotal());
//            JOptionPane.showMessageDialog(null, "El total es " + cuestionarioQuince.retornarTotal());
            Retornar retornarResultado = new Retornar(cuestionarioQuince, new Respuesta());
            retornarResultado.retonarResultadoFinal();
            Resultado ventanaDialog = new Resultado(this, true,retornarResultado.retonarResultadoFinal() );
            ventanaDialog.setVisible(true);
            
        }
    }//GEN-LAST:event_btnSiguiente15ActionPerformed


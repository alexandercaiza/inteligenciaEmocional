    private void btnPregunta1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPregunta1ActionPerformed
        if (validar.preguntaUno == false || validar.preguntaDos == false || validar.preguntaTres == false) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione todas las preguntas");

        } else {
            cuestionarioUno.retornarSubtotal();
            cuestionarioUno.retornarTotal();

//            JOptionPane.showMessageDialog(null, "El subtotal es " + cuestionarioUno.retornarSubtotal());
//            JOptionPane.showMessageDialog(null, "El total " + cuestionarioUno.retornarTotal());
            
            VentanaPreguntas2 ventanaDos = new VentanaPreguntas2(cuestionarioUno.retornarTotal());
            ventanaDos.setVisible(true);
            this.dispose();

        }


    }//GEN-LAST:event_btnPregunta1ActionPerformed

